CREATE TABLE HOADON_KHAMBENH(
  MAHDKB VARCHAR2(10) PRIMARY KEY,
  MAKHAM VARCHAR2(10) REFERENCES KHAM(MAKHAM),
  NGAYLAP DATE DEFAULT SYSDATE,
  TIENKHAM NUMBER (15, 3),
  TONGTIEN NUMBER (15,3),
  PHUONGTHUCTT NVARCHAR2(50),
  GHICHU NVARCHAR2(1000),
  TRANGTHAITT NVARCHAR2(50)
