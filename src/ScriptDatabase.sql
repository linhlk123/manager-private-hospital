CREATE TABLE DICHVUKHAM(
  MADV VARCHAR2(10) PRIMARY KEY,
  TENDV NVARCHAR2(100),
  MOTADV NVARCHAR2(1000),
  UUDAI NUMBER(3),
  DONGIA NUMBER(15, 3)
