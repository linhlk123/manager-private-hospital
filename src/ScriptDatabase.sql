CREATE TABLE KHAM(
  MAKHAM VARCHAR2(10) PRIMARY KEY,
  MALICH VARCHAR2(10) REFERENCES LICHHEN(MALICH),
  MABS VARCHAR2(10) REFERENCES BACSI(MABS),
