CREATE TABLE DIEUTRI(
  MADTR VARCHAR2(10) PRIMARY KEY,
  MAKHAM VARCHAR2(10) REFERENCES KHAM(MAKHAM),
  MABS VARCHAR2(10) REFERENCES BACSI(MABS),
